public class Food
{

    protected int number;
    protected String type;

    public Food (int number, String type){
        setNumber(number);
        setType(type);
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}
